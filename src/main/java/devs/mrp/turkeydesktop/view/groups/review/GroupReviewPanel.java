    @Override
    public Object getProperty(GroupReviewEnum property) {
        switch (property) {
            case GROUP_LABEL:
                return groupLabel;
            case PROCESS_PANEL:
                return processPanel;
            case TITLE_PANEL:
                return titlePanel;
            case CONDITIONS_PANEL:
                return conditionsPanel;
            case CONDITIONS_PANEL_LIST:
                return conditionsPanelList;
            case CONFIGURATION_PANEL:
                return configurationPanel;
            case TARGET_NAME_COMBO_BOX:
                return targetNameComboBox;
            case HOUR_SPINNER:
                return hourSpinner;
            case MINUTE_SPINNER:
                return minuteSpinner;
            case DAY_SPINNER:
                return daySpinner;
            case GROUP_NAME_TEXT:
                return groupNameText;
            case DELETE_TEXT:
                return deleteText;
            case EXTERNAL_TIME_PANEL:
                return externalTimePanel;
            case EXPORT_GROUP_TARGET:
                return exportGroupButton;
            case EXPORT_GROUP_DAYS:
                return exportGroupDaysSpinner;
            case ORDER_DROPDOWN:
                return orderDropdown;
            case TEXT_FILTER:
                return textFilter;
            default:
                return null;
        }
    }

